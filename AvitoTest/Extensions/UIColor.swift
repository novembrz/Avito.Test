import UIKit

extension UIColor {
    
    static func avitoBlueColor() -> UIColor{
        return #colorLiteral(red: 0.2980392157, green: 0.6705882353, blue: 1, alpha: 1)
    }
    
    static func cellBgColor() -> UIColor{
        return #colorLiteral(red: 0.9725490196, green: 0.9725490196, blue: 0.9725490196, alpha: 1)
    }
    
    static func textColor() -> UIColor{
        return .black
    }
    
    static func bgAndButtonTextColor() -> UIColor{
        return .white
    }
}
